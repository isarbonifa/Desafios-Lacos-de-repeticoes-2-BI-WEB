// Definição do array com a lista de empresas parceiras
const patrocinadores = ['Amazon', 'Google', 'Microsoft', 'GitHub'];

// Utilizando o laço 'for...of' para percorrer cada elemento do array
for (const empresa of patrocinadores) {
  
  // Impressão no console utilizando Template Literals (crases) para formatar a string
  console.log(`Patrocinador Ouro: ${empresa}`);
  
}